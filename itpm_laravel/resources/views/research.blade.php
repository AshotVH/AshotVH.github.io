                        collaboration proposal has been classified at Japanese government level as a science project of
                        the
                        highest priority, and was approved in early 2020.
                    </p>
                    <p class="history-desc">
                        Liquid Argon TPC technology, which was initially developed in Europe, now forms the basis for an
                        exciting new experiment, the international Deep Underground Neutrino Detector (DUNE) project.
                        Liquid
                        argon detectors feature outstanding pattern recognition and background rejection, and,
                        importantly for
                        long baseline experiments using spectral (L/E) information, an excellent total energy
                        reconstruction
                        over a wide range of energies. They thus they constitute a powerful exploration tool for
                        neutrino
                        oscillations, complementary to the more massive but lower-resolution water Cherenkov approach.
                        The DUNE
                        project is based on the earlier LBNE layout, with a 4x17 kton (40kton fiducial) liquid argon TPC
                        detector situated in the Stanford Underground Research Facility (SURF) at a distance of 1300 km
                        from
                        FNAL, where a new neutrino beam line and near-detector facilities would be constructed. Physics
                        goals
                        include measurement of the neutrino mass hierarchy, CP violation, nucleon decay, cosmic
                        neutrinos etc.
                        Currently at CERN two proto-DUNE detectors were constructed in order to verify new detection

                        technologies by using LHC proton beam facilities.</p>
