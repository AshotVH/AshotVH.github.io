                Our experimental physics activities are related to the neutrino physics. We are collaboration members of
                the main long baseline accelerator neutrino experiments and reactor (anti)neutrino experiment.

                HyperKamiokande is a new, megaton-scale water cherenkov detector project offering a 25-fold increase of
                fiducial volume over the existing 50 kiloton (22.5 kiloton fiducial) SuperKamiokande detector. This will
                provide decisive improvements in non-accelerator measurements such as the proton decay rate, and the
                detection of solar and atmospheric neutrinos and neutrinos from supernovae explosions. Exposed to the
                anticipated upgraded beam of T2K reaching a power 0.75MW-1.3MW, the T2HK project will allow a discovery
                of CP violation (5σ) in leptonic charged-current interactions. The HyperKamiokande international
                collaboration proposal has been classified at Japanese government level as a science project of the
                highest priority, and was approved in early 2020.

                Liquid Argon TPC technology, which was initially developed in Europe, now forms the basis for an
                exciting new experiment, the international Deep Underground Neutrino Detector (DUNE) project. Liquid
                argon detectors feature outstanding pattern recognition and background rejection, and, importantly for
                long baseline experiments using spectral (L/E) information, an excellent total energy reconstruction
                over a wide range of energies. They thus they constitute a powerful exploration tool for neutrino
                oscillations, complementary to the more massive but lower-resolution water Cherenkov approach. The DUNE
                project is based on the earlier LBNE layout, with a 4x17 kton (40kton fiducial) liquid argon TPC
                detector situated in the Stanford Underground Research Facility (SURF) at a distance of 1300 km from
                FNAL, where a new neutrino beam line and near-detector facilities would be constructed. Physics goals
                include measurement of the neutrino mass hierarchy, CP violation, nucleon decay, cosmic neutrinos etc.
                Currently at CERN two proto-DUNE detectors were constructed in order to verify new detection
                technologies by using LHC proton beam facilities.

                The Jiangmen Underground Neutrino Observatory (JUNO), a 20 kiloton multi-purpose underground liquid
                scintillator detector, was proposed with the determination of the neutrino mass hierarchy as a primary
                physics goal. It is also capable of observing neutrinos from terrestrial and extra-terrestrial sources,
                including supernova burst neutrinos, diffuse supernova neutrino background, geo-neutrinos, atmospheric
                neutrinos, solar neutrinos, as well as exotics such as dark matter and sterile neutrinos. It located
                south of Hong Kong at the distance 53 km from two main nuclear power plants.
